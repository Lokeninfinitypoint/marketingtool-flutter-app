  // Helper methods
  static List<ToolModel> getToolsByCategory(String categoryId) {
    return tools.where((tool) => tool.categoryId == categoryId).toList();
  }

  static List<ToolModel> searchTools(String query) {
    final lowerQuery = query.toLowerCase();
    return tools.where((tool) =>
      tool.name.toLowerCase().contains(lowerQuery) ||
      tool.description.toLowerCase().contains(lowerQuery) ||
      tool.tags.any((tag) => tag.toLowerCase().contains(lowerQuery))
    ).toList();
  }

  static List<ToolModel> getFeaturedTools() {
    return tools.where((tool) => tool.rating >= 4.7).take(10).toList();
  }

  static List<ToolModel> getPremiumTools() {
    return tools.where((tool) => tool.isPremium).toList();
  }

  static List<ToolModel> getFreeTools() {
    return tools.where((tool) => !tool.isPremium).toList();
  }

  static ToolCategoryModel? getCategoryById(String id) {
    try {
      return categories.firstWhere((cat) => cat.id == id);
    } catch (_) {
      return null;
    }
  }

  static int get totalToolsCount => tools.length;
  static int get premiumToolsCount => tools.where((t) => t.isPremium).length;
  static int get freeToolsCount => tools.where((t) => !t.isPremium).length;
